# ELIF = else if statement

nama = input("Namu kamu siapa? ")

if nama == "ucup":  # kondisi 1
    print("Hai ganteng")  # aksi true 1
elif nama == "otong":  # kondisi 2
    print("Hai si kece")  # aksi true 2
elif nama == "mario":  # kondisi 3
    print("Hai humor")  # aksi true 3
else:
    print("tidak kenal")  # aksi false

print("ini adalah akhir dari program")
